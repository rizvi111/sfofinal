        <div><strong>What is the Role / Responsibility of a Victim Witness Attorney?</strong>&nbsp;</div>
        <div>&nbsp;</div>
        <ul>
            <li style="margin-left:15px">Orient and explain to victims, witnesses, and family members the court process
                and their rights throughout that process&nbsp;</li>
            <li style="margin-left:15px">Court accompaniment for victims, witnesses and family members&nbsp;&nbsp;</li>
            <li style="margin-left:15px">Crisis intervention for victims, witnesses, and family members who are in need
                as a result of their victimization&nbsp;</li>
            <li style="margin-left:15px">Provide written and verbal notification to victims and witnesses regarding
                arrests, arraignments, pre-trial conferences, motions, hearings, grand jury appearances, trials,
                defaults, dispositions, restitution hearings, sentencing and appellate proceedings&nbsp;</li>
            <li style="margin-left:15px">Assess and evaluate victim needs and assist with referrals for appropriate
                services such as: shelter, mental health counseling, peer support groups, emergency financial
                assistance, protection, and relocation&nbsp;</li>
            <li style="margin-left:15px">Provide formal and informal education and training to criminal justice and
                community agencies regarding victim rights and victim witness services&nbsp;</li>
            <li style="margin-left:15px">Participate/ facilitate in community round tables&nbsp;</li>
        </ul>
